    private void boton_siguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_siguienteActionPerformed

        PreparedStatement ps;
        
        String c_rival = jTextField1.getText().trim();
        String c_condicion = jTextField2.getText().trim();
        String c_espectadores = jTextField3.getText().trim();
        String c_recaudacion = jTextField4.getText().trim();
        String c_instancia = jTextField6.getText().trim();
        String c_golesmarc = jTextField5.getText().trim();
        String c_golesrec = jTextField8.getText().trim();
        String c_estadio = jTextField7.getText().trim();
        String c_fecha = jTextField10.getText().trim();
        String c_nombretorneo = jTextField9.getText().trim();

        String sql = "insert into partido (partido.rival_partido,partido.condicion_partido,partido.espectadores_partido,partido.recaudacion_partido,partido.instancia_partido,partido.goles_marcados,partido.goles_recibidos,partido.estadio_partido,partido.fecha_partido,partido.id_torneo) SELECT ?,?,?,?,?,?,?,?,?,torneo.id_torneo FROM torneo WHERE torneo.nombre_torneo=?";
        
        try{
            con = conectar.getConexion();
            ps = con.prepareStatement(sql);
            ps.setString(1,c_rival);
            ps.setString(2,c_condicion);
            ps.setString(3,c_espectadores);
            ps.setString(4,c_recaudacion);
            ps.setString(5,c_instancia);
            ps.setString(6,c_golesmarc);
            ps.setString(7,c_golesrec);
            ps.setString(8,c_estadio);
            ps.setString(9,c_fecha);
            ps.setString(10,c_nombretorneo);
            ps.executeUpdate();         
            ps.close();
            con.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error de conexi√≥n: " + e.getMessage());
        }                                               

        
        VistaPartidos volver = new VistaPartidos();
        volver.setVisible(true);
        setVisible(false);
    }//GEN-LAST:event_boton_siguienteActionPerformed

