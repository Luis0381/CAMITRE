    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed

        ResultSet rs;
        PreparedStatement ps;
        
        String c_dni = jTextField1.getText().trim();
        String c_lesion = jTextField2.getText().trim();

        String sql = "insert into lesion (lesion.id_persona,lesion.tipo_lesion) SELECT id_persona,? FROM persona WHERE dni_persona=?";
        
        try{
            con = conectar.getConexion();
            ps = con.prepareStatement(sql);
            ps.setInt(2,parseInt(c_dni));
            ps.setString(1,c_lesion);
            ps.executeUpdate();         
            ps.close();
            con.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error de conexión: " + e.getMessage());
        }                                               

        
        VistaLesiones volver = new VistaLesiones();
        volver.setVisible(true);
        setVisible(false);
    }//GEN-LAST:event_AceptarActionPerformed

