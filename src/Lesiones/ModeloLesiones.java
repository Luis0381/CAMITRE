package Lesiones;

public class ModeloLesiones {
    private String nombre;
    private String apellido;
    private String apodo;
    private String tipo_lesion;
    private String inicio_tratamiento;
    private String fin_tratamiento;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getTipo_lesion() {
        return tipo_lesion;
    }

    public void setTipo_lesion(String tipo_lesion) {
        this.tipo_lesion = tipo_lesion;
    }

    public String getInicio_tratamiento() {
        return inicio_tratamiento;
    }

    public void setInicio_tratamiento(String inicio_tratamiento) {
        this.inicio_tratamiento = inicio_tratamiento;
    }

    public String getFin_tratamiento() {
        return fin_tratamiento;
    }

    public void setFin_tratamiento(String fin_tratamiento) {
        this.fin_tratamiento = fin_tratamiento;
    }

    public String getApodo() {
        return apodo;
    }

    public void setApodo(String apodo) {
        this.apodo = apodo;
    }
    
    
    
    


}