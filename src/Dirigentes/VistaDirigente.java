    private void boton_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_buscarActionPerformed

        
        String buscarNombre = jTextField1.getText().trim();
        String buscarApellido = jTextField2.getText().trim();
        String buscarCargo = jTextField4.getText().trim();
        String buscartipoCuenta = jTextField3.getText().trim();


        ModeloDirigente datos;
        ResultSet rs;
        PreparedStatement ps;
        ArrayList<ModeloDirigente> lista = new ArrayList<>();
        String sql = "select persona.nombre_persona,persona.apellido_persona,persona.genero_persona,persona.paisorigen_persona,dirigente.cargo_dirigente,dirigente.fecha_inicio,dirigente.fecha_fin,cuenta.tipo_cuenta FROM persona INNER JOIN dirigente ON persona.id_persona=dirigente.id_persona INNER JOIN cuenta ON dirigente.id_cuenta=cuenta.id_cuenta WHERE persona.nombre_persona LIKE ? AND persona.apellido_persona LIKE ? AND dirigente.cargo_dirigente LIKE ? AND cuenta.tipo_cuenta LIKE ?";
        
        try{
            con = conectar.getConexion();
            ps = con.prepareStatement(sql);
            ps.setString(1, "%" + buscarNombre + "%");
            ps.setString(2, "%" + buscarApellido + "%");
            ps.setString(3, "%" + buscarCargo + "%");
            ps.setString(4, "%" + buscartipoCuenta + "%");
            rs = ps.executeQuery();
            while(rs.next()){
                datos = new ModeloDirigente();
                datos.setNombre(rs.getString("persona.nombre_persona")); 
                datos.setApellido(rs.getString("persona.apellido_persona"));
                datos.setGenero(rs.getString("persona.genero_persona"));
                datos.setPais(rs.getString("persona.paisorigen_persona"));  
                datos.setCargo(rs.getString("dirigente.cargo_dirigente")); 
                datos.setInicio_contrato(rs.getString("dirigente.fecha_inicio")); 
                datos.setFin_contrato(rs.getString("dirigente.fecha_fin")); 
                datos.setTipo_cuenta(rs.getString("cuenta.tipo_cuenta")); 
                lista.add(datos);       
            }            
            rs.close();
            ps.close();
            con.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error de conexi√≥n: " + e.getMessage());
        }
        
        DefaultTableModel tabla =  new DefaultTableModel();
        String[] fila = new String[45];
        tabla.addColumn("Nombre");
        tabla.addColumn("Apellido");
        tabla.addColumn("Genero");
        tabla.addColumn("Pais de Origen");
        tabla.addColumn("Cargo");
        tabla.addColumn("Fecha Inicio");   
        tabla.addColumn("Fecha Fin");  
        tabla.addColumn("Tipo de Cuenta");  
        for(int f=0; f<lista.size();f++){
            fila[0] = lista.get(f).getNombre();
            fila[1] = lista.get(f).getApellido();
            fila[2] = lista.get(f).getGenero();
            fila[3] = lista.get(f).getPais();
            fila[4] = lista.get(f).getCargo();
            fila[5] = lista.get(f).getInicio_contrato();
            fila[6] = lista.get(f).getFin_contrato();
            fila[7] = lista.get(f).getTipo_cuenta();
            tabla.addRow(fila);
        }
        tblDatos.setModel(tabla);
    }//GEN-LAST:event_boton_buscarActionPerformed

